package ru.sfedu.studyProject.enums;

 /*
    INDIVIDUAL_PART - Любая часть фурсьюта отдельно
    MINI_PARTIAL - Включает в себя голову, лапы на руки, хвост
    PARTIAL - Включает в себя голову, лапы на руки, рукава, хвост
    FULL_PARTIAL - Включает в себя голову, лапы на руки и на ноги, рукава, хвост
    PLANTIGRADE_HALF - Включает в себя голову, лапы на руки и на ноги, рукава, хвост, прямые штаны
    DIGIGRADE_HALF - Включает в себя голову, лапы на руки и на ноги, рукава, хвост, штаны в иммитацией пальцеходящий лап
    UPPER_HALF - Включает в себя голову, лапы на руки и на ноги, рукава, хвост, жилетку
    PLANTIGRADE_FULL - Включает в себя голову, лапы на руки и на ноги, хвост, комбинезон на всё тело с прямыми ногами
    DIGIGRADE_FULL - Включает в себя голову, лапы на руки и на ноги, хвост, комбинезон на всё тело с иммитацией пальцеходящих лап
 */

public enum FursuitType {
    INDIVIDUAL_PART,
    MINI_PARTIAL,
    PARTIAL,
    FULL_PARTIAL,
    PLANTIGRADE_HALF,
    DIGIGRADE_HALF,
    UPPER_HALF,
    PLANTIGRADE_FULL,
    DIGIGRADE_FULL
}
